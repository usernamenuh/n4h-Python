a = 10
b = 3

print(a - b)  # Pengurangan
print(a + b)  # Penjumlahan
print(a * b)  # Perkalian
print(a / b)  # Pembagian (hasil float)
print(a // b) # Pembagian (hasil integer)
print(a % b)  # Modulus (sisa bagi)
print(a ** b) # Perpangkatan

