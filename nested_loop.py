# Tabel Perkalian legkap
print("Tabel perkalian 1-5:")
for i in range(1, 6):          # Looping untuk angka 1-5
    for j in range(1, 6):     # Looping untuk angka 1-10
        hasil = i * j # Menampilkan hasil perkalian
        print(i, "x", j, "=", hasil)
    print("---------------")  # Baris kosong setelah setiap tabel perkalian